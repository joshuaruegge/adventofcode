//long version of coord3. these numbers are big
class Coord3L {
	long x;
	long y;
	long z;
	
	public Coord3L(long x, long y, long z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public long dist(Coord3L o) {
		return Math.abs(x - o.x) + Math.abs(y - o.y) + Math.abs(z - o.z);
	}
	
	public Coord3L copy() {
		return new Coord3L(x,y,z);
	}
}
