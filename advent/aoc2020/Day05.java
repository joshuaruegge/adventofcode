	public int position(String s, int max) {
		int min = 0;
		for(char c : s.toCharArray()) {
			if(c == 'F' || c == 'L') {
				max -= (max - min + 1)/2;
			} else {
				min += (max - min + 1)/2;
			}
		}
		return min;
	}

